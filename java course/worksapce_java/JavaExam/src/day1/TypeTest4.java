package day1;

public class TypeTest4 {
	public static void main(String[] args) {	

		char testMunja;
		
		testMunja = '가';
		System.out.print(testMunja);
		System.out.print(testMunja);
		
		testMunja = 'A';
		System.out.print(testMunja);
		
		testMunja = '%';
		System.out.print(testMunja);
		
		testMunja = '\n';				//이스케이프 문자 : 원래 문자에 특별한 의미 부여
		System.out.print(testMunja);

		testMunja = '#';
		System.out.print(testMunja);
		
		testMunja = '\t';				//이스케이프 문자 : 원래 문자에 특별한 의미 부여
		System.out.print(testMunja);
		
		testMunja = '9';
		System.out.print(testMunja);
	}
}
